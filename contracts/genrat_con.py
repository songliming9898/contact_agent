def set_font(run, font_name='仿宋', font_size=Pt(12), bold=False, color=None):
    """设置字体样式，兼容中文字体"""
    run.font.name = font_name
    run._element.rPr.rFonts.set(qn('w:eastAsia'), font_name)
    run.font.size = font_size
    run.font.bold = bold
    if color:
        run.font.color.rgb = color

def add_paragraph_with_font(doc, text, font_name='仿宋', font_size=Pt(12), bold=False, alignment=WD_ALIGN_PARAGRAPH.LEFT, space_after=Pt(6)):
    """添加段落并设置样式"""
    p = doc.add_paragraph()
    p.alignment = alignment
    p.paragraph_format.space_after = space_after
    p.paragraph_format.line_spacing = 1.5
    run = p.add_run(text)
    set_font(run, font_name, font_size, bold)
    return p

def add_clause(doc, title, content):
    """添加合同条款（支持内容换行）"""
    p = doc.add_paragraph()
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(4)
    
    run_title = p.add_run(title + "：")
    set_font(run_title, '黑体', Pt(12), bold=True)
    
    lines = content.split('\n')
    for i, line in enumerate(lines):
        if i == 0:
            run_content = p.add_run(line)
            set_font(run_content, '仿宋', Pt(12))
        else:
            p_new = doc.add_paragraph()
            p_new.paragraph_format.line_spacing = 1.5
            p_new.paragraph_format.space_after = Pt(4)
            run_content = p_new.add_run(line)
            set_font(run_content, '仿宋', Pt(12))

def add_signature_table(doc, party_a, party_b, date):
    """使用表格生成整齐的落款"""
    table = doc.add_table(rows=3, cols=2)
    table.autofit = True
    
    # 设置表格内容
    cells = [
        (0, 0, f"甲方（盖章）：{party_a}"), (0, 1, f"乙方（盖章）：{party_b}"),
        (1, 0, "授权代表："), (1, 1, "授权代表："),
        (2, 0, f"日期：{date}"), (2, 1, f"日期：{date}")
    ]
    
    for row_idx, col_idx, text in cells:
        cell = table.cell(row_idx, col_idx)
        p = cell.paragraphs[0]
        p.paragraph_format.space_before = Pt(10)
        run = p.add_run(text)
        set_font(run, '仿宋', Pt(12))

